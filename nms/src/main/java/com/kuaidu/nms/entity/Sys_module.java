package com.kuaidu.nms.entity;
/*
 * 系统菜单表
 */
public class Sys_module {
	private Integer module_id;
	private String module_name;
	private Integer parent_id;
	private String module_url;
	private Integer show_order;
	private Integer flag;
	
   
	public Integer getModule_id() {
		return module_id;
	}


	public void setModule_id(Integer module_id) {
		this.module_id = module_id;
	}


	public String getModule_name() {
		return module_name;
	}


	public void setModule_name(String module_name) {
		this.module_name = module_name;
	}


	public Integer getParent_id() {
		return parent_id;
	}


	public void setParent_id(Integer parent_id) {
		this.parent_id = parent_id;
	}


	public String getModule_url() {
		return module_url;
	}


	public void setModule_url(String module_url) {
		this.module_url = module_url;
	}


	public Integer getShow_order() {
		return show_order;
	}


	public void setShow_order(Integer show_order) {
		this.show_order = show_order;
	}


	public Integer getFlag() {
		return flag;
	}


	public void setFlag(Integer flag) {
		this.flag = flag;
	}


	public Sys_module(Integer module_id, String module_name, Integer parent_id, String module_url, Integer show_order,
			Integer flag) {
		super();
		this.module_id = module_id;
		this.module_name = module_name;
		this.parent_id = parent_id;
		this.module_url = module_url;
		this.show_order = show_order;
		this.flag = flag;
	}


	public Sys_module() {
		super();
	}

}
