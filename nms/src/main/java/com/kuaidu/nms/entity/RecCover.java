package com.kuaidu.nms.entity;

public class RecCover {
	
	
	private Integer id;
	
	private String source_url;
	
	private String compress_url;
	
	private Integer is_promotion;
	
	private Integer status;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getSource_url() {
		return source_url;
	}

	public void setSource_url(String source_url) {
		this.source_url = source_url;
	}

	public String getCompress_url() {
		return compress_url;
	}

	public void setCompress_url(String compress_url) {
		this.compress_url = compress_url;
	}

	public Integer getIs_promotion() {
		return is_promotion;
	}

	public void setIs_promotion(Integer is_promotion) {
		this.is_promotion = is_promotion;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public RecCover(Integer id, String source_url, String compress_url, Integer is_promotion, Integer status) {
		super();
		this.id = id;
		this.source_url = source_url;
		this.compress_url = compress_url;
		this.is_promotion = is_promotion;
		this.status = status;
	}

	public RecCover() {
		super();
	}

	@Override
	public String toString() {
		return "RecCover [id=" + id + ", source_url=" + source_url + ", compress_url=" + compress_url
				+ ", is_promotion=" + is_promotion + ", status=" + status + "]";
	}

	
}
